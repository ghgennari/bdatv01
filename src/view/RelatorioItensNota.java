    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        int idNota = Integer.parseInt(txtIdNota.getText());

        ItensNotaDAO itemDAO = new ItensNotaDAO();
        List<ItensNota> itens = itemDAO.buscarPorNota(idNota);

        DefaultTableModel model = (DefaultTableModel) tblItensNota.getModel();
        model.setRowCount(0);
        
        
        
        for (ItensNota item : itens) {
            model.addRow(new Object[]{
                item.getId_produto(),
                item.getNomeProduto(),
                item.getQtdVenda(),
                item.getNomeCliente(),
                item.getData_venda()
            });
        }

        if (itens.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum item encontrado para esta nota.");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Digite um ID de nota v√°lido!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

